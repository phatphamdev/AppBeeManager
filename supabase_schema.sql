-- 5. Helper Functions / Triggers (Optional)
-- Auto-update updated_at on drivers
CREATE OR REPLACE FUNCTION update_modified_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ language 'plpgsql';

DROP TRIGGER IF EXISTS update_drivers_modtime ON public.drivers;
CREATE TRIGGER update_drivers_modtime
    BEFORE UPDATE ON public.drivers
    FOR EACH ROW
    EXECUTE FUNCTION update_modified_column();

